create or replace function public.can_access_huddle(target_huddle_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.huddles h
    where h.id = target_huddle_id
      and exists (
        select 1
        from public.huddle_members hm
        left join public.profiles current_user_profile on current_user_profile.id = auth.uid()
        where hm.huddle_id = h.id
          and hm.status = 'active'
          and (
            hm.member_id = auth.uid()
            or hm.member_tag = current_user_profile.tag
            or hm.member_phone_number = current_user_profile.phone_number
          )
      )
  );
$$;

create or replace function public.get_huddle_member_display_name(
  p_member_id uuid,
  p_member_tag text,
  p_member_phone_number text
)
returns text
language sql
stable
security definer
set search_path = public
as $$
  select coalesce(
    nullif(profile.display_name, ''),
    nullif(profile.tag, ''),
    nullif(profile.phone_number, ''),
    nullif(p_member_tag, ''),
    nullif(p_member_phone_number, ''),
    'Unknown member'
  )
  from (select 1) as input
  left join public.profiles profile on (
    profile.id = p_member_id
    or (p_member_tag is not null and p_member_tag <> '' and profile.tag = p_member_tag)
    or (
      p_member_phone_number is not null
      and p_member_phone_number <> ''
      and profile.phone_number = p_member_phone_number
    )
  )
  limit 1;
$$;


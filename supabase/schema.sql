-- Huddle profile and direct-network persistence.
-- Run this once in the Supabase SQL editor before testing the cloud-backed services.

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  display_name text not null default '',
  tag text not null default '',
  phone_number text not null default '',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.network_members (
  id bigint generated by default as identity primary key,
  owner_id uuid not null references public.profiles(id) on delete cascade,
  member_id uuid references public.profiles(id) on delete set null,
  member_tag text,
  member_phone_number text,
  created_at timestamptz not null default now(),
  constraint network_members_has_reference check (
    member_id is not null or member_tag is not null or member_phone_number is not null
  )
);

create unique index if not exists network_members_owner_member_key
  on public.network_members(owner_id, member_id)
  where member_id is not null;

create unique index if not exists network_members_owner_tag_key
  on public.network_members(owner_id, member_tag)
  where member_tag is not null;

create unique index if not exists network_members_owner_phone_key
  on public.network_members(owner_id, member_phone_number)
  where member_phone_number is not null;

alter table public.profiles enable row level security;
alter table public.network_members enable row level security;

create policy "Authenticated users can read profiles"
  on public.profiles for select
  to authenticated
  using (true);

create policy "Users can create their own profile"
  on public.profiles for insert
  to authenticated
  with check (auth.uid() = id);

create policy "Users can update their own profile"
  on public.profiles for update
  to authenticated
  using (auth.uid() = id)
  with check (auth.uid() = id);

create policy "Users can read their network"
  on public.network_members for select
  to authenticated
  using (auth.uid() = owner_id);

create policy "Users can add to their network"
  on public.network_members for insert
  to authenticated
  with check (auth.uid() = owner_id);

create policy "Users can remove from their network"
  on public.network_members for delete
  to authenticated
  using (auth.uid() = owner_id);

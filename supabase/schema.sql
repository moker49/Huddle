create function public.list_visible_huddles()
returns table (
  id uuid,
  title text,
  owner_id uuid,
  owner_tag text,
  owner_phone_number text,
  created_at timestamptz,
  auto_archive_at timestamptz,
  member_ids text[],
  unread_count integer
)
language sql
stable
security definer
set search_path = public
as $$
  select
    h.id,
    h.title,
    h.owner_id,
    owner_profile.tag,
    owner_profile.phone_number,
    h.created_at,
    h.auto_archive_at,
    coalesce(
      array_agg(
        coalesce(hm.member_id::text, hm.member_tag, hm.member_phone_number)
        order by hm.created_at
      ) filter (where hm.id is not null),
      '{}'::text[]
    ),
    (
      select count(*)::integer
      from public.huddle_messages message
      left join public.huddle_read_states read_state on (
        read_state.huddle_id = h.id
        and read_state.profile_id = auth.uid()
      )
      where message.huddle_id = h.id
        and message.created_at > coalesce(read_state.last_read_at, '-infinity'::timestamptz)
        and message.author_id is distinct from auth.uid()
    )
  from public.huddles h
  join public.profiles owner_profile on owner_profile.id = h.owner_id
  left join public.huddle_members hm on hm.huddle_id = h.id and hm.status = 'active'
  where public.can_access_huddle(h.id)
  group by h.id, owner_profile.tag, owner_profile.phone_number;
$$;

create or replace function public.sync_current_user_huddle_network()
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  current_profile public.profiles;
begin
  if auth.uid() is null then
    return;
  end if;

  select *
  into current_profile
  from public.profiles
  where id = auth.uid();

  if not found then
    return;
  end if;

  with shared_huddles as (
    select h.id
    from public.huddles h
    where exists (
        select 1
        from public.huddle_members hm
        where hm.huddle_id = h.id
          and hm.status = 'active'
          and (
            hm.member_id = current_profile.id
            or (
              current_profile.tag <> ''
              and hm.member_tag = current_profile.tag
            )
            or (
              current_profile.phone_number <> ''
              and hm.member_phone_number = current_profile.phone_number
            )
          )
      )
  ),
  candidate_members as (
    select
      coalesce(resolved_member.id, hm.member_id) as member_id,
      case
        when resolved_member.id is null and hm.member_id is null then hm.member_tag
        else null
      end as member_tag,
      case
        when resolved_member.id is null and hm.member_id is null then hm.member_phone_number
        else null
      end as member_phone_number
    from shared_huddles h
    join public.huddle_members hm on hm.huddle_id = h.id and hm.status = 'active'
    left join public.profiles resolved_member on (
      resolved_member.id = hm.member_id
      or (
        hm.member_tag is not null
        and hm.member_tag <> ''
        and resolved_member.tag = hm.member_tag
      )
      or (
        hm.member_phone_number is not null
        and hm.member_phone_number <> ''
        and resolved_member.phone_number = hm.member_phone_number
      )
    )
  )
  insert into public.network_members (
    owner_id,
    member_id,
    member_tag,
    member_phone_number
  )
  select
    current_profile.id,
    candidate.member_id,
    candidate.member_tag,
    candidate.member_phone_number
  from candidate_members candidate
  where (candidate.member_id is not null
      or candidate.member_tag is not null
      or candidate.member_phone_number is not null)
    and (
      candidate.member_id is null
      or candidate.member_id <> current_profile.id
    )
    and (
      candidate.member_tag is null
      or candidate.member_tag <> current_profile.tag
    )
    and (
      candidate.member_phone_number is null
      or candidate.member_phone_number <> current_profile.phone_number
    )
  on conflict do nothing;
end;
$$;

grant execute on function public.sync_current_user_huddle_network() to authenticated;

create or replace function public.create_huddle(
  p_title text,
  p_auto_archive_at timestamptz,
  p_members jsonb
)
returns table (
  id uuid,
  title text,
  owner_id uuid,
  created_at timestamptz,
  auto_archive_at timestamptz
)
language plpgsql
security definer
set search_path = public
as $$
declare
  new_huddle public.huddles;
begin
  if auth.uid() is null then
    raise exception 'An authenticated account is required.';
  end if;

  if coalesce(trim(p_title), '') = '' then
    raise exception 'Huddle title is required.';
  end if;

  if jsonb_array_length(coalesce(p_members, '[]'::jsonb)) = 0 then
    raise exception 'At least one huddle member is required.';
  end if;

  insert into public.huddles (owner_id, title, auto_archive_at)
  values (auth.uid(), trim(p_title), p_auto_archive_at)
  returning * into new_huddle;

  insert into public.huddle_members (
    huddle_id,
    member_id,
    member_tag,
    member_phone_number
  )
  values (new_huddle.id, auth.uid(), null, null)
  on conflict do nothing;

  insert into public.huddle_members (
    huddle_id,
    member_id,
    member_tag,
    member_phone_number
  )
  select
    new_huddle.id,
    nullif(member.value ->> 'member_id', '')::uuid,
    nullif(member.value ->> 'member_tag', ''),
    nullif(member.value ->> 'member_phone_number', '')
  from jsonb_array_elements(p_members) as member(value)
  on conflict do nothing;

  insert into public.huddle_messages (
    huddle_id,
    body,
    kind,
    activity_type,
    author_id,
    author_name
  )
  values (new_huddle.id, 'Huddle created', 'system', 'huddle_created', auth.uid(), 'System');

  return query
  select
    new_huddle.id,
    new_huddle.title,
    new_huddle.owner_id,
    new_huddle.created_at,
    new_huddle.auto_archive_at;
end;
$$;

grant execute on function public.create_huddle(text, timestamptz, jsonb) to authenticated;

create or replace function public.update_huddle(
  p_huddle_id uuid,
  p_title text,
  p_auto_archive_at timestamptz,
  p_members jsonb
)
returns table (
  id uuid,
  title text,
  owner_id uuid,
  created_at timestamptz,
  auto_archive_at timestamptz
)
language plpgsql
security definer
set search_path = public
as $$
declare
  existing_huddle public.huddles;
begin
  if auth.uid() is null then
    raise exception 'An authenticated account is required.';
  end if;

  select *
  into existing_huddle
  from public.huddles as huddle
  where huddle.id = p_huddle_id;

  if not found then
    raise exception 'Huddle could not be found.';
  end if;

  if not public.can_access_huddle(p_huddle_id) then
    raise exception 'You are not allowed to update this huddle.';
  end if;

  if coalesce(trim(p_title), '') = '' then
    raise exception 'Huddle title is required.';
  end if;

  if jsonb_array_length(coalesce(p_members, '[]'::jsonb)) = 0 then
    raise exception 'At least one huddle member is required.';
  end if;

  if existing_huddle.title <> trim(p_title) then
    insert into public.huddle_messages (
      huddle_id,
      body,
      kind,
      activity_type,
      author_id,
      author_name
    )
    values (
      p_huddle_id,
      format('Title updated from "%s" to "%s"', existing_huddle.title, trim(p_title)),
      'system',
      'title_updated',
      auth.uid(),
      'System'
    );
  end if;

  if existing_huddle.auto_archive_at is distinct from p_auto_archive_at then
    insert into public.huddle_messages (
      huddle_id,
      body,
      kind,
      activity_type,
      author_id,
      author_name
    )
    values (
      p_huddle_id,
      case
        when p_auto_archive_at is null then 'Auto-archive removed'
        when existing_huddle.auto_archive_at is null then format(
          'Auto-archive set for %s',
          to_char(p_auto_archive_at at time zone 'UTC', 'Mon FMDD, YYYY')
        )
        else format(
          'Auto-archive updated from %s to %s',
          to_char(existing_huddle.auto_archive_at at time zone 'UTC', 'Mon FMDD, YYYY'),
          to_char(p_auto_archive_at at time zone 'UTC', 'Mon FMDD, YYYY')
        )
      end,
      'system',
      'auto_archive_updated',
      auth.uid(),
      'System'
    );
  end if;

  insert into public.huddle_messages (
    huddle_id,
    body,
    kind,
    activity_type,
    author_id,
    author_name
  )
  select
    p_huddle_id,
    format(
      'Member added: %s',
      public.get_huddle_member_display_name(
        nullif(member.value ->> 'member_id', '')::uuid,
        nullif(member.value ->> 'member_tag', ''),
        nullif(member.value ->> 'member_phone_number', '')
      )
    ),
    'system',
    'member_added',
    auth.uid(),
    'System'
  from jsonb_array_elements(p_members) as member(value)
  where not exists (
    select 1
    from public.huddle_members existing_member
    where existing_member.huddle_id = p_huddle_id
      and existing_member.status = 'active'
      and coalesce(
        existing_member.member_id::text,
        existing_member.member_tag,
        existing_member.member_phone_number
      ) = coalesce(
        nullif(member.value ->> 'member_id', ''),
        nullif(member.value ->> 'member_tag', ''),
        nullif(member.value ->> 'member_phone_number', '')
      )
  );

  insert into public.huddle_messages (
    huddle_id,
    body,
    kind,
    activity_type,
    author_id,
    author_name
  )
  select
    p_huddle_id,
    format(
      'Member removed: %s',
      public.get_huddle_member_display_name(
        existing_member.member_id,
        existing_member.member_tag,
        existing_member.member_phone_number
      )
    ),
    'system',
    'member_removed',
    auth.uid(),
    'System'
  from public.huddle_members existing_member
  where existing_member.huddle_id = p_huddle_id
    and existing_member.status = 'active'
    and not exists (
      select 1
      from jsonb_array_elements(p_members) as member(value)
      where coalesce(
        nullif(member.value ->> 'member_id', ''),
        nullif(member.value ->> 'member_tag', ''),
        nullif(member.value ->> 'member_phone_number', '')
      ) = coalesce(
        existing_member.member_id::text,
        existing_member.member_tag,
        existing_member.member_phone_number
      )
    );

  update public.huddles as huddle
  set title = trim(p_title), auto_archive_at = p_auto_archive_at
  where huddle.id = p_huddle_id;

  update public.huddle_members existing_member
  set status = 'left', left_at = now()
  where existing_member.huddle_id = p_huddle_id
    and existing_member.status = 'active'
    and not exists (
      select 1
      from jsonb_array_elements(p_members) as member(value)
      where coalesce(
        nullif(member.value ->> 'member_id', ''),
        nullif(member.value ->> 'member_tag', ''),
        nullif(member.value ->> 'member_phone_number', '')
      ) = coalesce(
        existing_member.member_id::text,
        existing_member.member_tag,
        existing_member.member_phone_number
      )
    );

  update public.huddle_members existing_member
  set status = 'active', left_at = null
  where existing_member.huddle_id = p_huddle_id
    and existing_member.status = 'left'
    and exists (
      select 1
      from jsonb_array_elements(p_members) as member(value)
      where coalesce(
        nullif(member.value ->> 'member_id', ''),
        nullif(member.value ->> 'member_tag', ''),
        nullif(member.value ->> 'member_phone_number', '')
      ) = coalesce(
        existing_member.member_id::text,
        existing_member.member_tag,
        existing_member.member_phone_number
      )
    );

  insert into public.huddle_members (
    huddle_id,
    member_id,
    member_tag,
    member_phone_number
  )
  select
    p_huddle_id,
    nullif(member.value ->> 'member_id', '')::uuid,
    nullif(member.value ->> 'member_tag', ''),
    nullif(member.value ->> 'member_phone_number', '')
  from jsonb_array_elements(p_members) as member(value)
  where not exists (
    select 1
    from public.huddle_members existing_member
    where existing_member.huddle_id = p_huddle_id
      and coalesce(
        existing_member.member_id::text,
        existing_member.member_tag,
        existing_member.member_phone_number
      ) = coalesce(
        nullif(member.value ->> 'member_id', ''),
        nullif(member.value ->> 'member_tag', ''),
        nullif(member.value ->> 'member_phone_number', '')
      )
  )
  on conflict do nothing;

  return query
  select
    h.id,
    h.title,
    h.owner_id,
    h.created_at,
    h.auto_archive_at
  from public.huddles h
  where h.id = p_huddle_id;
end;
$$;


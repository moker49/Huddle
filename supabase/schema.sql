  on public.huddles for select
  to authenticated
  using (public.can_access_huddle(id));

create policy "Authenticated users can create huddles"
  on public.huddles for insert
  to authenticated
  with check (auth.uid() = owner_id);

create policy "Members can update visible huddles"
  on public.huddles for update
  to authenticated
  using (public.can_access_huddle(id))
  with check (public.can_access_huddle(id));

create policy "Members can delete visible huddles"
  on public.huddles for delete
  to authenticated
  using (public.can_access_huddle(id));

create policy "Members can read visible huddle members"
  on public.huddle_members for select
  to authenticated
  using (public.can_access_huddle(huddle_id));

create policy "Huddle members can be added to visible huddles"
  on public.huddle_members for insert
  to authenticated
  with check (public.can_access_huddle(huddle_id));

create policy "Huddle members can be removed from visible huddles"
  on public.huddle_members for delete
  to authenticated
  using (public.can_access_huddle(huddle_id));

create policy "Members can read visible huddle messages"
  on public.huddle_messages for select
  to authenticated
  using (public.can_access_huddle(huddle_id));

create policy "Members can create user messages"
  on public.huddle_messages for insert
  to authenticated
  with check (
    public.can_access_huddle(huddle_id)
    and kind = 'user'
    and activity_type is null
    and author_id = auth.uid()
  );

create policy "Users can read their huddle read state"
  on public.huddle_read_states for select
  to authenticated
  using (profile_id = auth.uid());

create policy "Users can create their huddle read state"
  on public.huddle_read_states for insert
  to authenticated
  with check (profile_id = auth.uid() and public.can_access_huddle(huddle_id));

create policy "Users can update their huddle read state"
  on public.huddle_read_states for update
  to authenticated
  using (profile_id = auth.uid())
  with check (profile_id = auth.uid() and public.can_access_huddle(huddle_id));
